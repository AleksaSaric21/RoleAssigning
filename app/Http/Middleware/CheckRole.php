<?php

namespace App\Http\Middleware;

use Closure;

class CheckRole
{
    /**
     * Handle an incoming request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Closure  $next
     * @return mixed
     */
    public function handle($request, Closure $next, $roles)
    {

        return $request->user()->authorizeWithRoles($roles) ?
            $next($request) : redirect('home')->withErrors('Not authorized');
    }
}
